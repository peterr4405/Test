package challenge;


public class challenge1 {
    
    public static void main(String[] args) {
//        9*9 乖法表

        for(int i=1;i<=9;i++){
            for(int j=1;j<=9;j++){
                System.out.printf("%d * %d = %d ",i,j,i*j);
            }
            System.out.println();
        }

    }
    
}
